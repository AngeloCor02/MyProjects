package esercizi;

public class Esercizio_numero_uno {

	public static void main(String[] args) {
		int x= 3;
		boolean y= true;
		System.out.println(x);
	
/*****************************MOLTIPLICAZIONE*****************************/
		System.out.print(x * 8 + "\n");
	
/*****************************BOOLEANO*****************************/

		System.out.print((3 < 8) + " Angelo\n");

		System.out.print((9 < 10 && 114 < x) + " Angelo\n");
		
		System.out.println(y);
/************************TIPI DI VARIABILI******************/
/************************VARIABILI PRIMITIVE******************/

		byte ilMioByte = 69;
		System.out.println(ilMioByte);

		short casa = 169;
		System.out.println(casa);

		long cane= 1_936_193_634_314_123_123L;
		System.out.println(cane);

		char lettera= 'a';
		System.out.println(lettera);
		
		float num= 3.43f;
		System.out.println(num);
		
		double numDec= 3.431111112321332123421123123;
		System.out.println(numDec);
/************************VARIABILI REFERENCE******************/

		String saluto = "ciao";
		System.out.println(saluto);
		
		
	}//Qui finisce il main. 

}
